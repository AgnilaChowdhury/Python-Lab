import math

# a) 5 to the power of 8
result_a = 5 ** 8
print(f"5 to the power of 8 is: {result_a}")

# b) square root of 400
result_b = math.sqrt(400)
print(f"Square root of 400 is: {result_b}")

# c) exponent of 5 (assuming you mean natural exponent, i.e. e^5)
result_c = math.exp(5)
print(f"Exponent of 5 (e^5) is: {result_c}")

# d) Logarithm of 625 base 5 (assuming you mean log base 5)
result_d = math.log(625, 5)
print(f"Logarithm of 625 base 5 is: {result_d}")